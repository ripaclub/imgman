namespace ImgMan\Operation\Helper\Options;

trait FormatOptionTrait
{
    use AbstractOptionTrait;

    protected $format;

    /**
     * @param string $format
     */
    public function setFormat($format)
    {
        $this->format = $format;
    }

    /**
     * @return string
     */
    public function getFormat()
    {
        return $this->format;
    }
}