namespace ImgManLibrary\Operation\Helper\Options;


use Zend\Stdlib\AbstractOptions;

trait WidthOptionTrait
{
    use AbstractOptionTrait;

    protected $width;

    /**
     * @param mixed $width
     */
    public function setWidth($width)
    {
        $this->width = $width;
    }

    /**
     * @return mixed
     */
    public function getWidth()
    {
        return $this->width;
    }
}